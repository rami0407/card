export function t(key: keyof typeof translations.ar, lang: LangType, replacements?: Record<string, string | number>): string {
  const dict = translations[lang] || translations.ar;
  let text = dict[key] || translations.ar[key] || (key as string);
  
  if (replacements) {
    Object.keys(replacements).forEach(r => {
      text = text.replace(`{${r}}`, String(replacements[r]));
    });
  }
  
  return text;
}
